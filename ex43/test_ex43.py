# -- Оружейная лаборатория
# -- Топливный отсек
#
#
# ИЕРАРХИЯ КЛАССОВ
# -- Карта
# -- Движок
# -- Сцена
# 	- Смерть
# 	- Центральный коридор
# 	- Оружейная лаборатория
# 	- Топливный отсек
# 	- Спасательная капсула
#
#
# СХЕМА ОБЪЕКТОВ
# -- Карта (Мар)
# 	- next_scene (перейти к следующей сцене из карты)
# 	- opening_scene (открыть сцену)
# -- Движок (Engine)
# 	- play (запустить движок)
# -- Сцена (Scene)
# 	- enter (войти на сцену)
# 	- Смерть (Death)
# 	- Центральный коридор (CentralCorridor)
# 	- Оружейная лаборатория (LaserWeaponArmory)
# 	- Топливный отсек (TheFuelcell)
# 	- Спасательная капсула (EscapePod)


