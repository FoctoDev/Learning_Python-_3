# СЦЕНАРИЙ
# «Инопланетяне (Готоны) захватили космический корабль. На этом корабле игровой
# персонаж должен пробраться через лабиринт отсеков и сбежать в спасательной
# капсуле на планету. Игровой процесс будет похож на приключенческие игры наподобие
# Zork или Adventure с текстовым интерфейсом и смешными выражениями.
# Игра будет включать в себя движок, запускающий карту сцен. В каждой сцене будет
# выводиться собственное описание, когда игрок попадет в нее, а затем будет
# осуществляться переход в другую сцену».
#
#
# ОПИСАНИЕ СЦЕН
# -- Смерть. Когда игрок умирает, должна выводиться какая-нибудь забавная фраза.
# -- Центральный коридор. Здесь будет начинаться игра. Тут будет находиться один
#   из Готонов и наш герой. Игрок должен победить Готона с помощью шутки, прежде
#   чем продолжить играть.
# -- Оружейная лаборатория. Здесь герой получает нейтронную бомбу, чтобы заминировать
#   корабль, прежде чем попасть в спасательную капсулу. Потребуется угадать числовой
#   код доступа к бомбе.
# -- Топливный отсек. Другая сцена битвы с Готонами, в которой герой устанавливает бомбу.
# -- Отсек спасательных капсул. Здесь герой убегает с корабля, но только если он
#   выбрал капсулу с правильным номером.
#
#
# КЛЮЧЕВЫЕ КОНЦЕПЦИИ (СПИСОК СУЩЕСТВИТЕЛЬНЫХ)
# -- Инопланетянин
# -- Игрок
# -- Корабль
